<template>
    <div class="col-sm-6 col-md-4">
        <div class="panel panel-success">
            <div class="panel-heading">
                <h3 class="panel-title"> {{stock.name}}
                </h3>
            </div>
            <div class="panel-body">
            <div class="pull-left">
                <input type="number"
                        class="form-control"
                        placeholder="Quantity"
                        v-model="quantity">
            </div>
            <div class="pull-right">
                <button class="btn btn-success" 
                        @click="buyStock"
                        :disabled="insufficientFunds || quantity <=0 || ((quantity - Math.floor(quantity)) !== 0) " >{{insufficientFunds?'Not enough':'Buy'}}</button>
            </div>
        </div>
        </div>
        
    </div>
</template>

<script>
export default {
    props:['stock'],

    data(){
        return{
            quantity:0
        }
    },

    computed:{
        insufficientFunds(){
            return this.quantity*this.stock.price > this.$store.getters.funds;
        }
    },
    methods:{
        buyStock(){
            const order = {
                stockId:this.stock.id,
                price: this.stock.price,
                quantity:this.quantity
            }

            this.$store.dispatch('buyStock',order);
            // console.log(order);
            this.quantity = 0;
        }
    }
    
}
</script>

